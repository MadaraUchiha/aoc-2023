var data = input.split("\n").map((row) => row.split(""));

function tiltNorth(data) {
  for (let n = 0; n < data.length; n++) {
    for (let i = 1; i < data.length; i++) {
      for (let j = 0; j < data[0].length; j++) {
        if (data[i][j] === "O" && data[i - 1][j] === ".") {
          data[i - 1][j] = "O";
          data[i][j] = ".";
        }
      }
    }
  }
}
function tiltSouth(data) {
  for (let n = 0; n < data.length; n++) {
    for (let i = 0; i < data.length - 1; i++) {
      for (let j = 0; j < data[0].length; j++) {
        if (data[i][j] === "O" && data[i + 1][j] === ".") {
          data[i + 1][j] = "O";
          data[i][j] = ".";
        }
      }
    }
  }
}

function tiltEast(data) {
  for (let n = 0; n < data.length; n++) {
    for (let i = 0; i < data.length; i++) {
      for (let j = 1; j < data[0].length; j++) {
        if (data[i][j] === "O" && data[i][j - 1] === ".") {
          data[i][j - 1] = "O";
          data[i][j] = ".";
        }
      }
    }
  }
}

function tiltWest(data) {
  for (let n = 0; n < data.length; n++) {
    for (let i = 0; i < data.length; i++) {
      for (let j = 0; j < data[0].length - 1; j++) {
        if (data[i][j] === "O" && data[i][j + 1] === ".") {
          data[i][j + 1] = "O";
          data[i][j] = ".";
        }
      }
    }
  }
}
